package uz.example.steam_learning.controller;

import uz.example.steam_learning.exception.ResourceNotFoundException;
import uz.example.steam_learning.model.User;
import uz.example.steam_learning.repository.UserRepository;
import uz.example.steam_learning.security.CurrentUser;
import uz.example.steam_learning.security.UserPrincipal;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.access.prepost.PreAuthorize;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class UserController {

    @Autowired
    private UserRepository userRepository;

    @GetMapping("/user/me")
    @PreAuthorize("hasRole('USER')")
    public User getCurrentUser(@CurrentUser UserPrincipal userPrincipal) {
        return userRepository.findById(userPrincipal.getId())
                .orElseThrow(() -> new ResourceNotFoundException("User", "id", userPrincipal.getId()));
    }
}
